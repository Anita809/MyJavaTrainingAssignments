package com.java.Liscenceonline;

public class Liscence { //POJO -plain old java object

	private int liscenceId;
	private String liscenceDate;
	private String liscenceExpiryDate;
	private String liscenceStatus;
	public int getLiscenceId() {
		return liscenceId;
	}
	public void setLiscenceId(int liscenceId) {
		this.liscenceId = liscenceId;
	}
	public String getLiscenceDate() {
		return liscenceDate;
	}
	public void setLiscenceDate(String liscenceDate) {
		this.liscenceDate = liscenceDate;
	}
	public String getLiscenceExpiryDate() {
		return liscenceExpiryDate;
	}
	public void setLiscenceExpiryDate(String liscenceExpiryDate) {
		this.liscenceExpiryDate = liscenceExpiryDate;
	}
	public String getLiscenceStatus() {
		return liscenceStatus;
	}
	public void setLiscenceStatus(String liscenceStatus) {
		this.liscenceStatus = liscenceStatus;
	}
	
	
	
}
